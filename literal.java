public class literal {
    public static void main(String[] args) {
        char ch = 'V';
        int a = 20;
        double d1 = 10.3;
        float f1 = 12.4f;

        System.out.println(ch);
        System.out.println(a);
        System.out.println(d1);
        System.out.println(f1);

    }
}
